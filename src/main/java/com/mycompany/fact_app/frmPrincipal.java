    private void btnCierreCajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCierreCajaActionPerformed
        // Obtén los datos de la tabla
        DefaultTableModel modeloTabla = (DefaultTableModel) tblFactura.getModel();
        DefaultTableModel modeloTablaGastos = (DefaultTableModel) tblGastos.getModel();
        int rowCount = modeloTabla.getRowCount();
        ArrayList<String> datosReporte = new ArrayList<>();
        ArrayList<String> datosReporteGastos = new ArrayList<>();

        // Agrega los datos de la tabla a la lista
        for (int i = 0; i < rowCount; i++) {
            String fila = String.format("Cliente: %s, Correo: %s, Producto: %s, Cantidad: %s, Precio Unitario: %s, Total con IVA: %s",
                    modeloTabla.getValueAt(i, 0), modeloTabla.getValueAt(i, 1),
                    modeloTabla.getValueAt(i, 2), modeloTabla.getValueAt(i, 3),
                    modeloTabla.getValueAt(i, 4), modeloTabla.getValueAt(i, 5));
            datosReporte.add(fila);
        }

        // Agrega los datos de la tabla de gastos a la lista
        int rowCountGastos = modeloTablaGastos.getRowCount();
        for (int x = 0; x < rowCountGastos; x++) {
            String filaGasto = String.format("Motivo Gasto: %s, Monto: %s",
                    modeloTablaGastos.getValueAt(x, 0), modeloTablaGastos.getValueAt(x, 1));
            datosReporteGastos.add(filaGasto);
        }

        // Mostrar un cuadro de diálogo para confirmar el cierre de caja
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Desea realizar el cierre de caja?",
                "Confirmar cierre de caja",
                JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Si el usuario selecciona 'Sí', continuar con la lógica de cierre de caja

            // Genera el reporte y lo guarda en un archivo de texto
            reporteTxt.generarReporte(datosReporte, datosReporteGastos, "cierre_caja", lblTotalIngresos.getText(), lblTotalGastos.getText(), lblTotalGanancias.getText());
        } else {
            // Si el usuario selecciona 'No', puedes realizar alguna acción adicional o simplemente salir
            System.out.println("El usuario ha cancelado el cierre de caja.");
        }
    }//GEN-LAST:event_btnCierreCajaActionPerformed

