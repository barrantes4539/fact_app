    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
     

        // Mostrar un cuadro de diálogo para confirmar la impresión
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Desea imprimir la factura?",
                "Confirmar impresión",
                JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Si el usuario selecciona 'Sí', continuar con la lógica de impresión

            // Obtener los valores seleccionados y escritos
            String nombreUsuario = txtNuevoCliente.getText();
            String correoUsuario = txtCorreoNuevo.getText();
            String telefono = txtTelefono.getText();
            String id = txtNuevoID.getText();
            String producto = cmbxProductos.getSelectedItem().toString();
            String precioU = txtPrecioU.getText();
            String cantidad = txtCantidad.getText();
            int precioUnitario = Integer.parseInt(txtPrecioU.getText());
            int cantidadP = Integer.parseInt(txtCantidad.getText());

            // Llamar al método para agregar un nuevo usuario
            datosUsuarios.agregarNuevoUsuario(nombreUsuario, telefono, id, correoUsuario);

            // Llamar al método para agregar el cálculo
            datosUsuarios.adicionCalculo(nombreUsuario, producto, precioUnitario, cantidadP);

            // Obtener el modelo de la tabla
            DefaultTableModel modeloTabla = (DefaultTableModel) tblFactura.getModel();

            // Obtener los cálculos del usuario
            ArrayList<String> calculos = datosUsuarios.buscarUsuarioPorNombre(nombreUsuario).calculos;

            // Obtener la posición de la última fila en la tabla
            int ultimaFila = modeloTabla.getRowCount();

            // Iterar sobre los cálculos del usuario y agregar las filas a la tabla
            for (String calculo : calculos) {
                // Supongo que los datos están separados por comas o algún carácter
                String[] partes = calculo.split(", ");

                // Agregar una fila con los datos del cálculo
                modeloTabla.addRow(partes);
            }

            // Sumar el total con IVA y actualizar el totalIngresos
            totalIngresos += Double.parseDouble(modeloTabla.getValueAt(ultimaFila, 5).toString());

            // Actualizar el labelTotalIngresos con el total acumulado
            lblTotalIngresos.setText(String.valueOf(totalIngresos));

            // Limpiar los campos después de agregar los cálculos
            txtNuevoCliente.setText("");
            txtCorreoNuevo.setText("");
            txtTelefono.setText("");
            txtNuevoID.setText("");
            txtCantidad.setText("");
            txtPrecioU.setText("");

            int ganancias = totalIngresos - totalGastos;
            lblTotalGanancias.setText(String.valueOf(ganancias));
            // Desplazar la selección a la última fila agregada
            tblFactura.changeSelection(ultimaFila, 0, false, false);
        } else {
            // Si el usuario selecciona 'No' simplemente no hace nada
            System.out.println("El usuario ha cancelado la impresión de la factura.");
        }
    }//GEN-LAST:event_btnImprimirActionPerformed

