    private void btnContabilizarGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContabilizarGastoActionPerformed
        // Obtener los valores seleccionados y escritos
        String motivoGasto = txtMotivoGasto.getText();
        int montoGasto = Integer.parseInt(txtMontoGasto.getText());

        // Mostrar un cuadro de diálogo para confirmar la contabilización del gasto
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Desea contabilizar el gasto?",
            "Confirmar contabilización de gasto",
            JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Si el usuario selecciona 'Sí', continuar con la lógica de contabilización del gasto

            // Obtener el modelo de la tabla de gastos
            DefaultTableModel modeloTablaGastos = (DefaultTableModel) tblGastos.getModel();

            // Supongo que los datos están separados por comas o algún carácter
            String[] partes = {motivoGasto, String.valueOf(montoGasto)};

            // Agregar una fila con los datos del gasto
            modeloTablaGastos.addRow(partes);

            // Desplazar la selección a la última fila agregada
            tblGastos.changeSelection(modeloTablaGastos.getRowCount() - 1, 0, false, false);

            // Sumar el monto del gasto y actualizar el totalGastos
            totalGastos += montoGasto;

            // Actualizar el labelTotalGastos con el total acumulado
            lblTotalGastos.setText(String.valueOf(totalGastos));

            // Calcular las ganancias y actualizar el labelTotalGanancias
            double ganancias = totalIngresos - totalGastos;
            lblTotalGanancias.setText(String.valueOf(ganancias));

            // Limpiar los campos después de contabilizar el gasto
            txtMotivoGasto.setText("");
            txtMontoGasto.setText("");
        } else {
            // Si el usuario selecciona 'No' simplemente salir
            System.out.println("El usuario ha cancelado la contabilización del gasto.");
        }
    }//GEN-LAST:event_btnContabilizarGastoActionPerformed

